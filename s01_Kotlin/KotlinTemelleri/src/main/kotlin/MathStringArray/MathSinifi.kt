package MathStringArray

fun main() {
    println(Math.E)
    println(Math.PI)
    println(Math.E / Math.PI)
    println(Math.abs(-9))
    println(Math.ceil(1.1))
    println(Math.floor(1.9))
    println(Math.pow(2.0,5.0))
    println(Math.sqrt(25.0))
    println(Math.max(10,15))
    println(Math.min(10,15))
    println(Math.random() * 100)
    println(Math.pow(5.0,2.0) / 2)
}