package Operatorler

fun main() {

    println("Ve operatörü &&")
    println(false && false)
    println(false && true)
    println(true && false)
    println(true && true)


    println()
    println("Veya operatörü ||")
    println(false || false)
    println(false || true)
    println(true || false)
    println(true || true)

    println()
    println("Eşit operatörü ==")
    println(false == false)
    println(false == true)
    println(true == false)
    println(true == true)

    println()

    println("Eşit değil operatörü !=")
    println(false != false)
    println(false != true)
    println(true != false)
    println(true != true)

    println("Eşit değil !")
    println(!false)
    println(!true)

}