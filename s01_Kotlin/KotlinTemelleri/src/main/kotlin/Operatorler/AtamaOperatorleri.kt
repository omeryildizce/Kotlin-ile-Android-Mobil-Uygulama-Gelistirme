package Operatorler

fun main() {
    var sayi = 5

    // +=, -=, *=, /=, %=
    println("Sayı = 5 için,")
    sayi += 5
    println("+= 5 = " + sayi)
    sayi -= 5
    println("-= 5 = " + sayi)
    sayi *= 5
    println("*= 5 = " + sayi)
    sayi /= 5
    println("/= 5 = " + sayi)
    sayi %= 5
    println("%= 5 = " + sayi)

}